<?php

    $a = 60;
    $b = 16;

    // bitwise end
    $c = $a & $b;
    echo "$a & $b = $c";
    echo "<br>";


    // bitwise or
    $c = $a | $b;
    echo "$a | $b = $c";
    echo "<br>";

    //bitwise xor
    $c = $a ^ $b;
    echo "$a ^ $b = $c";
    echo "<br>";
    
    //shift left
    $c = $a << $b;
    echo "$a << $b = $c";
    echo "<br>";
    
    //shift right
    $c = $a >> $b;
    echo "$a >> $b = $c";
    echo "<br>"; 




?>